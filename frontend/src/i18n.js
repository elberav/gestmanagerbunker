// Store reactivo de Svelte — cuando cambie, TODA la UI se re-renderiza
export const lang = writable(localStorage.getItem('app-lang') || 'es');

/**
 * Store derivado: devuelve una función t(key) que siempre usa el idioma actual.
 * En los componentes usar como: {$t('appTitle')}
 */
export const t = derived(lang, ($lang) => {
  return (key) => {
    const trans = translations[$lang]?.[key] || translations['es'][key] || key;
    return trans;
  };
});

export function setLang(newLang) {
  if (translations[newLang]) {
    lang.set(newLang);
    localStorage.setItem('app-lang', newLang);
  }
}

export function initLang() {
  const saved = localStorage.getItem('app-lang');
  if (saved && translations[saved]) {
    lang.set(saved);
  }
}